    const showProposalModal = ref(false);
    
    // 캘린더 상태
    const currentDate = ref(new Date());
    const selectedDay = ref(null);
    const memberStatuses = ref({});
    const actualMembers = ref([]);
    const restaurants = ref([]);
    const proposals = ref([]);
    const loading = ref(false);
    
